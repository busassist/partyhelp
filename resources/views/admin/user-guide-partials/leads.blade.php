    <h2 class="text-xl font-semibold border-b border-gray-200 dark:border-gray-700 pb-2">3. Leads</h2>
    <p>Use <strong>Leads</strong> in the sidebar to list, create, and edit lead records.</p>

    <h3>3.1 List view</h3>
    <p>The leads table shows key fields: customer name/email, occasion type, suburb, guest count, budget range, status, base/current price, purchase count, expiry, and timestamps. Use filters and sort to find specific leads (e.g. by status or date).</p>

    <h3>3.2 Lead statuses</h3>
    <ul>
        <li><strong>New</strong> — Lead created but not yet distributed (e.g. no matching venues).</li>
        <li><strong>Distributed</strong> — Matched venues have been notified; lead is available for purchase.</li>
        <li><strong>Partially fulfilled</strong> — At least one venue has purchased, but purchase target not yet reached.</li>
        <li><strong>Fulfilled</strong> — Purchase target reached; lead is closed to further purchases.</li>
    </ul>

    <h3>3.3 Create / Edit</h3>
    <p>You can manually create a lead or edit an existing one. Important fields include: contact details, occasion type, suburb, guest count, budget range, room styles, base price, current price, discount percent, status, purchase target, and expiry. Editing is useful for corrections or testing. Changing status or pricing affects how venues see the lead and what they pay.</p>
</section>
