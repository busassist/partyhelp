    <h2 class="text-xl font-semibold border-b border-gray-200 dark:border-gray-700 pb-2">2. Dashboard</h2>
    <p>After logging in at <strong>/admin</strong>, the dashboard is your home screen. It shows high-level stats and widgets such as:</p>
    <ul>
        <li>Lead counts (e.g. new, distributed, fulfilled).</li>
        <li>Venue and room counts.</li>
        <li>Other operational metrics the system exposes.</li>
    </ul>
    <p>Use the dashboard to get a quick overview of platform activity. For detailed management, use the sidebar to open <strong>Leads</strong>, <strong>Venues</strong>, <strong>Manage System Data</strong>, and <strong>Manage System Pricing</strong>.</p>
